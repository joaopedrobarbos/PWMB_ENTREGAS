const portfolioKey = 'investmentPortfolio';

        function loadPortfolio() {
            const portfolio = localStorage.getItem(portfolioKey);
            return portfolio ? JSON.parse(portfolio) : [];
        }

        function savePortfolio(portfolio) {
            localStorage.setItem(portfolioKey, JSON.stringify(portfolio));
        }

        function renderPortfolio() {
            const portfolio = loadPortfolio();
            const portfolioList = document.getElementById('portfolio-list');
            portfolioList.innerHTML = '';
            portfolio.forEach((asset, index) => {
                const li = document.createElement('li');
                li.textContent = `${asset.name}: ${asset.quantity} unidades @ R$${asset.purchasePrice} - Atual: R$${asset.currentPrice}`;
                const removeButton = document.createElement('button');
                removeButton.textContent = 'Remover';
                removeButton.onclick = () => removeAsset(index);
                li.appendChild(removeButton);
                portfolioList.appendChild(li);
            });
            renderPerformance();
        }

        function addAsset(event) {
            event.preventDefault();
            const name = document.getElementById('name').value;
            const quantity = parseFloat(document.getElementById('quantity').value);
            const purchasePrice = parseFloat(document.getElementById('purchase-price').value);
            const currentPrice = parseFloat(document.getElementById('current-price').value);

            const portfolio = loadPortfolio();
            portfolio.push({ name, quantity, purchasePrice, currentPrice });
            savePortfolio(portfolio);
            renderPortfolio();
        }

        function removeAsset(index) {
            const portfolio = loadPortfolio();
            portfolio.splice(index, 1);
            savePortfolio(portfolio);
            renderPortfolio();
        }

        function renderPerformance() {
            const portfolio = loadPortfolio();
            let totalInvestment = 0;
            let currentTotalValue = 0;

            portfolio.forEach(asset => {
                totalInvestment += asset.quantity * asset.purchasePrice;
                currentTotalValue += asset.quantity * asset.currentPrice;
            });

            const performance = ((currentTotalValue - totalInvestment) / totalInvestment) * 100;

            const portfolioPerformance = document.getElementById('portfolio-performance');
            portfolioPerformance.textContent = `Valor Total Atual: R$${currentTotalValue.toFixed(2)} - Performance: ${performance.toFixed(2)}%`;
        }

        document.getElementById('add-asset-form').addEventListener('submit', addAsset);

    
        renderPortfolio();
    

        
        changeLanguage('pt-BR');


        function mostrarResultados() {
            const fundsTableBody = document.getElementById('fundsTable').getElementsByTagName('tbody')[0];

   
            fundsTableBody.innerHTML = '';

            const funds = JSON.parse(localStorage.getItem('funds')) || [];

            funds.forEach(fund => {
                const newRow = fundsTableBody.insertRow();

                newRow.insertCell(0).innerText = fund.fundName;
                newRow.insertCell(1).innerText = fund.investmentStrategy;
                newRow.insertCell(2).innerText = fund.managementFee + '%';
                newRow.insertCell(3).innerText = fund.riskProfile;
            });
        }

        document.addEventListener('DOMContentLoaded', mostrarResultados);
        
        document.getElementById('fundForm').addEventListener('submit', function(event) {
            event.preventDefault();

            const fundName = document.getElementById('fundName').value;
            const investmentStrategy = document.getElementById('investmentStrategy').value;
            const managementFee = document.getElementById('managementFee').value;
            const riskProfile = document.getElementById('riskProfile').value;

            const newFund = {
                fundName,
                investmentStrategy,
                managementFee,
                riskProfile
            };

            const funds = JSON.parse(localStorage.getItem('funds')) || [];
            funds.push(newFund);
            localStorage.setItem('funds', JSON.stringify(funds));

            document.getElementById('fundForm').reset();
        });